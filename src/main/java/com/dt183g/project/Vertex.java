package com.dt183g.project;

public class Vertex {
    private final int xCoordinate;
    private final int yCoordinate;

    public Vertex(int x, int y) {
        this.xCoordinate = x;
        this.yCoordinate = y;
    }

    public int getXCoordinate() {
        return xCoordinate;
    }

    public int getYCoordinate() {
        return yCoordinate;
    }
}
